package com.sorin.idea.util;

public class InfoCentro {

	private String centro;
	private String direccion;
	private String ciudad;
	private String pais;
	private String telefono;
	private String responsable;
	private String servicio;
	
	public InfoCentro(){
	}
	public InfoCentro(String c,String d, String ciu, String p, String t,String r){
		this.setCentro(c);
		this.setDireccion(d);
		this.setCiudad(ciu);
		this.setPais(p);
		this.setTelefono(t);
		this.setResponsable(r);
	}
	public InfoCentro(String c,String d, String ciu, String p, String t,String r,String s){
		this.setCentro(c);
		this.setDireccion(d);
		this.setCiudad(ciu);
		this.setPais(p);
		this.setTelefono(t);
		this.setResponsable(r);
		this.setServicio(s);
	}
	
	public String getCentro() {
		return centro;
	}
	public void setCentro(String centro) {
		this.centro = centro;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getResponsable() {
		return responsable;
	}
	public void setResponsable(String responsable) {
		this.responsable = responsable;
	}
	public String getServicio() {
		return servicio;
	}
	public void setServicio(String servicio) {
		this.servicio = servicio;
	}
	
}
