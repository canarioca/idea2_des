package com.sorin.idea.util;

import java.io.Serializable;
import java.util.ArrayList;

import com.sorin.idea.dto.ArrRegistrador;
import com.sorin.idea.dto.SegSeguimientoRegEventos;

public class RegistradorSeguimientosBean implements Serializable{

	private ArrRegistrador reg;
	private ArrayList<SegSeguimientoRegEventos> segs;
	
	public RegistradorSeguimientosBean(){
		this.segs = new ArrayList<SegSeguimientoRegEventos>();
	}

	public ArrRegistrador getReg() {
		return reg;
	}

	public void setReg(ArrRegistrador reg) {
		this.reg = reg;
	}

	public ArrayList<SegSeguimientoRegEventos> getSegs() {
		return segs;
	}

	public void setSegs(ArrayList<SegSeguimientoRegEventos> segs) {
		this.segs = segs;
	}
	
}
